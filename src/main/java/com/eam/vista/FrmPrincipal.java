    private void btnIniciar_PrincipalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciar_PrincipalActionPerformed
        correo = txtCorreo_login.getText();
        String password = txtContrasena_login.getText();
        String EMAIL_PATTERN
                = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";

        CtlUsuario ctlusuario = new CtlUsuario();
        boolean resp = ctlusuario.Loguear(correo, password);
        if (correo.isEmpty() && password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar todos los campos");
        } else {

            if (correo.equalsIgnoreCase("Admin") && password.equalsIgnoreCase("1234")) {
                Limpiar();
                FrmAdministrador frmadministrador = new FrmAdministrador(this);
                frmadministrador.setLocationRelativeTo(null);
                frmadministrador.setVisible(true);

                this.dispose();
            } else if (correo.matches(EMAIL_PATTERN)) {
                if (resp) {
                    Limpiar();
                    FrmCliente frmcliente = new FrmCliente(this, true, correo);
                    frmcliente.setLocationRelativeTo(null);
                    frmcliente.setVisible(true);

                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Credenciales incorrectas");
                }
            } else {
                Limpiar();
                JOptionPane.showMessageDialog(null, "Debe ingresar un correo valido");
            }

        }
    }//GEN-LAST:event_btnIniciar_PrincipalActionPerformed

