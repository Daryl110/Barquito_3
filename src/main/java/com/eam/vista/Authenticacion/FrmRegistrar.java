    private void btn_RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RegistrarActionPerformed
        if (txtCedula_Reg.getText().isEmpty() || txtNombre_Reg.getText().isEmpty()
                || txtCiudad_Reg.getText().isEmpty() || txtContrasena_Reg.getText().isEmpty()
                || txtCorreo_Reg.getText().isEmpty() || txtTelefono_Reg.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe rellenar todos los campos!!");
            return;
        }
        String cedula = txtCedula_Reg.getText().trim(),
                nombre = txtNombre_Reg.getText().trim(),
                ciudad = txtCiudad_Reg.getText().trim(),
                contrasena = txtContrasena_Reg.getText().trim(),
                correo = txtCorreo_Reg.getText().trim(),
                telefono = txtTelefono_Reg.getText().trim();
        String EMAIL_PATTERN
                = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";

        if (correo.matches(EMAIL_PATTERN)) {

            Usuario usuario = new Usuario(correo, contrasena);
            Cliente cliente = new Cliente(cedula, telefono, ciudad, nombre, usuario);

            boolean registroUsuario = ((boolean) (Main.dao.guardar(usuario).get("Resultado"))),
                    registroCliente = cliente.registrar();

            if (registroUsuario && registroCliente) {
                JOptionPane.showMessageDialog(this, "El cliente " + nombre+" Con el correo de "+ correo + " se ha registrado con exito");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "No se ha podido registrar el cliente");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debe ingresar un email valido");
        }
        
    }//GEN-LAST:event_btn_RegistrarActionPerformed

    private void txtCedula_RegKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCedula_RegKeyTyped
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c)
                || (c == KeyEvent.VK_BACK_SPACE)
                || (c == KeyEvent.VK_DELETE))) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_txtCedula_RegKeyTyped

