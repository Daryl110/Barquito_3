    public Cliente traerCliente() {
        ArrayList<String> campos = new ArrayList<>(), valores = new ArrayList<>();
        campos.add("usuariocorreo.correo");
        valores.add(correo);
        ArrayList<Cliente> cliente = new ArrayList(Main.dao.cargar("Cliente", campos, valores));
        return cliente.get(0);
    }

    public String traerMail() {
        String mensaje = "";
        ArrayList<String> campos = new ArrayList<>(), valores = new ArrayList<>();
        campos.add("usuariocorreo.correo");
        valores.add(correo);
        ArrayList<Mail> listaMail = new ArrayList(Main.dao.cargar("Mail", campos, valores));
        for (int i = 0; i < listaMail.size(); i++) {
            int x = i + 1;
            mensaje += "Mensaje " + x;
            mensaje += "\n";
            mensaje += listaMail.get(i).toString();
            mensaje += "\n";
            mensaje += "";
            mensaje += "\n";
        }
        return mensaje;
    }

    public void limpiarCampos() {
        txtPeso_Cliente.setText("");
        txtTamano_Cliente.setText("");
    }


